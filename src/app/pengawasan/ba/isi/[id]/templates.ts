export function getTemplate(kategori: string) {
  const k = (kategori || '').toLowerCase();
  if (k.includes('industri')) return TEMPLATE_INDUSTRI;
  if (k.includes('fasyankes') || k.includes('kesehatan')) return TEMPLATE_FASYANKES;
  if (k.includes('perumahan') || k.includes('pengembang')) return TEMPLATE_PERUMAHAN;
  if (k.includes('toko') || k.includes('pasar')) return TEMPLATE_TOKO_MODERN;
  if (k.includes('sppg')) return TEMPLATE_SPPG;
  return TEMPLATE_INDUSTRI; // default fallback
}
